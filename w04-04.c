#include <stdio.h>

int main() {
    // กรณีที่ 1: ใช้ int กับค่าทศนิยม
    int intScore = 90.5;  // ผิดประเภท: ทศนิยมจะถูกตัดเหลือแค่ 90
    printf("กรณีที่ 1 (int): Score = %d\n", intScore);

    // กรณีที่ 2: ใช้ float กับค่าทศนิยม
    float floatScore = 90.5;  // ถูกต้อง: float รองรับทศนิยม
    printf("กรณีที่ 2 (float): Score = %.1f\n", floatScore);

    // สรุปผลการทดลอง
    //printf("\nสรุป:\n");
   // printf("- ใช้ int กับค่าทศนิยมจะทำให้ส่วนทศนิยมหายไปเพราะ int นับแค่จำนวนเต็ม (ได้แค่ 90)\n");
    //printf("- ใช้ float จะเก็บและแสดงค่าทศนิยมได้ถูกต้องเพราะว่า float โจทย์ให้มานับค่าทศนิยม 1ตำแหน่ง(ได้ 90.5)\n");
   //printf("- จึงต้องเลือกชนิดข้อมูลให้ตรงกับประเภทของค่าที่ต้องการเก็บ\n");

    return 0;
}
