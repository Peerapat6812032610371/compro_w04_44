// 2.เพราะว่าค่าที่ได้จะตรงกับที่โจทย์ต้องการมากที่สุดเช่นโจทย์ต้องการทศนิยมเราก็ต้องเลือกใช้ .1f เป้นต้น
// 3.เราต้องเลือกใช้ให้ถูกตัวและเหมาะสมกับชนิดของข้อมูลที่ต้องการ